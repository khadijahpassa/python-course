num1 = float(input("Enter the first number:"))
num2 = float(input("Enter the second number:"))

add = num1 + num2
print(f"{num1} + {num2} = {add}")

subs = num1 - num2
print(f"{num1} - {num2} = {subs}")

multiply = num1 * num2
print(f"{num1} * {num2} = {multiply}")

div = num1 / num2
print(f"{num1} / {num2} = {div}")

floor_div = num1 // num2
print(f"{num1} // {num2} = {floor_div}")
